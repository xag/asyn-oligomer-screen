// Vicinity-molecule candidate list — the search space for the Stage 3
// perturbation engine described in ../IN_SILICO_PLAN.md.
//
// Scope: molecules already present in human physiology or diet that may bias
// α-synuclein's conformational ensemble.
//
// Inclusion criterion (the only gate):
//
//   Plausibly reaches the substantia nigra / striatum at concentrations
//   achievable through diet, supplementation, endogenous synthesis,
//   lifestyle (exercise, stress, sleep, hormonal state), or environmental
//   exposure (occupational, water, food residue). That's it. Prior
//   α-synuclein evidence is NOT required — discovering whether a molecule
//   shifts P(active) is the search. Filtering to known α-syn modulators
//   would reduce the pipeline to recapitulation, which defeats the
//   purpose.
//
// Direction of effect is open. Stage 3 scores ΔP(active | M), which can
// be positive (the molecule biases the ensemble toward toxic conformers
// — "harmful") or negative ("protective"). Both belong in the list on
// the same footing: a ranked list of harm-leaning candidates is as
// useful for prevention as a ranked list of protective ones is for
// treatment, and the search would be incoherent if only one sign were
// admissible.
//
// Two roles per entry (`role` field):
//
//   "anchor"     Validation control. Has published evidence of modulating
//                α-syn aggregation, binding it directly, or being
//                epidemiologically associated with PD risk. Stage 3 must
//                reproduce the published sign on these or its other rankings
//                are not trustworthy.
//   "candidate"  Search-space entry. Reaches the CNS at achievable
//                concentrations; α-syn effect unknown or only weakly
//                hinted. The pipeline's job is to estimate ΔP(active | M)
//                and produce a ranked list of these.
//   "both"       Has published α-syn evidence AND remains an interesting
//                search-space entry. Positive prediction is novel detail
//                beyond what's known; negative prediction is a sign the
//                pipeline is undercalling.
//
// Validation hold-out (`validation_holdout: true`):
//
//   Marks entries added AFTER the Stage 2/3 framework was locked, intended
//   as blind tests of the existing scoring rules. The framework will not
//   be retuned in response to where these molecules rank — that is the
//   point of a hold-out. Distinct from `role: "anchor"` which only
//   designates published α-syn evidence: a role="anchor" entry seeded
//   before the framework was locked is not a hold-out, because its
//   presence could in principle have influenced feature selection or
//   threshold choices.
//
//   The first hold-out cohort (2026-05-27) consists of five gold-standard
//   α-syn modulators with strong published evidence that were either
//   present with smiles:null (EGCG, silibinin, rosmarinic-acid) or absent
//   entirely (fisetin, CAPE). Their docking and sweep ranking is the
//   blind validation of the framework before any wet-lab handoff.
//
// Field contract:
//
//   id              kebab-case slug, URL-safe canonical key. Used in
//                   compute_job:* and result:* records.
//   name            display name.
//   group           one of the six VICINITY_GROUPS below.
//   role            "anchor" | "candidate" | "both".
//   smiles          SMILES string. Null when ambiguous (lipid class spanning
//                   acyl chains), unsafe to seed without verification
//                   (complex natural products, large cofactors), or just
//                   pending — Stage 3 docking needs a structure but the
//                   moderation pipeline can fill these in.
//   pdb_ligand      PDB chemical-component code, when one exists. Stage 3
//                   docking prefers a curated PDB ligand over a fresh
//                   parameterisation.
//   mw_da           average molecular weight in daltons. Null for ions
//                   (the atomic mass is the meaningful number) and lipid
//                   classes.
//   cns_conc        { low, high, note }. low/high are strings with units so
//                   the entry preserves order of magnitude rather than
//                   implying false precision. `note` records the
//                   compartment ("striatal extracellular", "cytosolic",
//                   "synaptic vesicle") or condition. low/high may be null
//                   when the molecule is established to reach the CNS but
//                   precise concentrations are uncharacterised.
//                   USED BY STAGE 3 SCORING — Stage 3 docks and scores at
//                   the achievable CNS concentration, not at whatever
//                   concentration in vitro α-syn papers happened to use.
//   delivery        { route, feasibility, notes }. PURE METADATA — does not
//                   enter the Stage 3 score. Drives search/dispatch priority
//                   (high-feasibility candidates run first) and result
//                   presentation (callers can filter on feasibility). A
//                   top-ranked low-bioavailability hit still publishes; it
//                   simply carries the flag that reaching it from diet
//                   requires a delivery breakthrough — itself a finding.
//                     route: one of VICINITY_ROUTES (endogenous, diet,
//                            supplement, precursor, microbiome, injection,
//                            none-known).
//                     feasibility: one of VICINITY_FEASIBILITIES (native,
//                            achievable, low-bioavailability, invasive-only,
//                            unknown).
//                     notes: short free-text on the practical route,
//                            bioavailability gap, or dosing context.
//   evidence        one-line note. For anchors: the published α-syn effect.
//                   For candidates: the physiological reason it's in scope.
//                   Honest about what's known vs hypothesised.
//   refs            free-text "Author et al., Journal Year — Title".
//                   Required for "anchor" and "both" (≥1). Optional for
//                   "candidate" — the physiology itself is the justification.
//                   Kept as free text on purpose: the moderation pipeline
//                   canonicalises into PMIDs/DOIs over time, but seeding
//                   author/year/journal avoids fabricating IDs.
//   corpus_articles local article ids in the reading-site corpus (Redis
//                   `article:*`). Empty at seed; populated when reviewers
//                   tag articles with the molecule.
//
// Coverage: ~150 entries at seed. Matches the plan's "hundreds of plausible
// candidates, not millions." The moderation pipeline can extend without
// bound inside the inclusion criterion. Obvious near-term expansions:
// additional flavonoid stereoisomers, more bile-acid conjugates, finer
// lipid-class breakdowns (acyl-chain pairings), kynurenine pathway
// intermediates beyond the four seeded.
//
// Additions go through admin review (TODO: extend lib/submission.js with
// kind: "vicinity_molecule" and surface in admin.js). Until that lands,
// edit this file directly and commit.

