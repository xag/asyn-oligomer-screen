export function vicinityById(id) {
  return VICINITY_MOLECULES.find((m) => m.id === id) || null;
}

export function vicinityByGroup(group) {
  return VICINITY_MOLECULES.filter((m) => m.group === group);
}

export function vicinityByRole(role) {
  if (role === 'anchor' || role === 'both')
    return VICINITY_MOLECULES.filter((m) => m.role === 'anchor' || m.role === 'both');
  if (role === 'candidate')
    return VICINITY_MOLECULES.filter((m) => m.role === 'candidate' || m.role === 'both');
  return [];
}

export function vicinityByFeasibility(feasibility) {
  return VICINITY_MOLECULES.filter((m) => m.delivery && m.delivery.feasibility === feasibility);
}
